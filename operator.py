# 연산자
print(6/3)

print(2**3) # 2^3 제곱 연산 기능
print(5%3) # 나머지 구하기
print(10%3) # 1
print(5//3) # 몫 구하기
print(10//3)

# 비교 연산
print(10 >= 3)
print(10 < 3)
print(5 <= 5)

print(3 == 3) # same True
print(4 == 6) # False

print(1 != 3) # not same True
print(not(1 != 3)) # False

print((3 > 0) and (3 < 5)) # True
print((3 > 0) & (3 < 5)) # True

print((3 > 0) or (3 > 5)) # True
print((3 > 0) | (3 > 5)) # True

# 비교연산자 연속으로 사용가능
print(5 > 4 > 3)
print(5 > 4 > 7)

